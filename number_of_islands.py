from typing import List

class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        def detect_cell(x, y, grid): 
            pos = []
            if y > 0 and grid[y - 1][x] == "1":
                pos += [(x, y - 1)]
                grid[y - 1][x] = "0"
            if y < len(grid) - 1 and grid[y + 1][x] == "1": 
                pos += [(x, y + 1)]
                grid[y + 1][x] = "0"
            if x > 0 and grid[y][x - 1] == "1": 
                pos += [(x - 1, y)]
                grid[y][x - 1] = "0"
            if x < len(grid[0]) - 1 and grid[y][x + 1] == "1": 
                pos += [(x + 1, y)]
                grid[y][x + 1] = "0"
            return pos
        
        n_island = 0
        # cnt = 0
        for y in range(len(grid)): 
            for x in range(len(grid[0])):
                # cnt += 1
                if grid[y][x] == "1": # detect islands
                    n_island += 1
                    q = [(x, y)]
                    grid[y][x] = "0"
                    while len(q) > 0: 
                        x1, x2 = q.pop(0)
                        pos = detect_cell(x1, x2, grid)
                        q += pos 
                    # print(x, y)
                    # for row in grid: 
                    #     print(row)
                    # print()
    
        return n_island

